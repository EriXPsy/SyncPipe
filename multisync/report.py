def _fig_to_base64(fig) -> str:
    """Encode a matplotlib figure as a base64 PNG string."""
    buf = io.BytesIO()
    fig.savefig(buf, format="png", dpi=150, bbox_inches="tight")
    buf.seek(0)
    b64 = base64.b64encode(buf.read()).decode("ascii")
    plt.close(fig)
    return f'<img src="data:image/png;base64,{b64}" alt="figure"/>'


def _plot_group_comparison(test_results: List[Any], label_a: str, label_b: str) -> str:
    """Bar chart comparing means across groups."""
    if not _MPL_AVAILABLE or not test_results:
        return "<p class='warning'>No test results — figure skipped.</p>"

    sig = [r for r in test_results if r.significant_fdr]
    all_r = sig if sig else test_results[:8]

    metrics = [r.metric.replace("_", " ").capitalize() for r in all_r]
    means_a = [r.mean_a for r in all_r]
    means_b = [r.mean_b for r in all_r]

    x = np.arange(len(metrics))
    width = 0.35

    fig, ax = plt.subplots(figsize=(max(5, len(metrics) * 1.5), 4))
    bars_a = ax.bar(x - width/2, means_a, width, label=label_a,
                    color="#378ADD", edgecolor="#333", linewidth=0.5)
    bars_b = ax.bar(x + width/2, means_b, width, label=label_b,
                    color="#D85A30", edgecolor="#333", linewidth=0.5)

    ax.set_xticks(x)
    ax.set_xticklabels(metrics, rotation=30, ha="right", fontsize=9)
    ax.set_ylabel("Mean value", fontsize=10)
    ax.set_title(
        f"Group comparison: {label_a} vs {label_b}\n"
        + ("(significant metrics only)" if sig else "(all metrics)"),
        fontsize=11,
    )
    ax.legend(fontsize=9)

    # Significance stars
    for i, r in enumerate(all_r):
        if r.significant_fdr:
            max_val = max(r.mean_a, r.mean_b)
            ax.text(i, max_val * 1.05, "*", ha="center", fontsize=12,
                    color="#005500")

    fig.tight_layout()
    return _fig_to_base64(fig)


# ---------------------------------------------------------------------------
# HTML building helpers
# ---------------------------------------------------------------------------

def _html_table(rows: List[Dict[str, Any]], highlight_key: Optional[str] = None,
                highlight_val: Any = True) -> str:
    if not rows:
        return "<p>No data.</p>"
    keys = list(rows[0].keys())
    header = "<tr>" + "".join(f"<th>{k}</th>" for k in keys) + "</tr>"
    body = ""
    for row in rows:
        row_class = ""
        if highlight_key and row.get(highlight_key) == highlight_val:
            row_class = ' class="sig"'
        cells = ""
        for k in keys:
            v = row[k]
            if isinstance(v, float):
                cells += f"<td>{v:.4f}</td>" if not np.isnan(v) else "<td>—</td>"
            elif isinstance(v, bool):
                cells += f"<td>{'✓' if v else ''}</td>"
            else:
                cells += f"<td>{v}</td>"
        body += f"<tr{row_class}>{cells}</tr>"
    return f"<table><thead>{header}</thead><tbody>{body}</tbody></table>"


def _html_wrap(title: str, body: str, version: str) -> str:
    ts = datetime.now().strftime("%Y-%m-%d %H:%M")
    footer = _FOOTER_TEMPLATE.format(version=version, timestamp=ts)
    return f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8"/>
<title>{title} — SyncPipe Report</title>
<style>{_CSS}</style>
</head>
<body>
{body}
{footer}
</body>
</html>"""


