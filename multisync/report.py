class ReportGenerator:
    """
    Generate publication-ready HTML reports from SyncPipe results.

    Parameters
    ----------
    output_dir : str
        Default output directory for reports.
    """

    def __init__(self, output_dir: str = "reports/") -> None:
        self.output_dir = Path(output_dir)

    def single_dyad_report(
        self,
        results: AnalysisResults,
        filepath: Optional[str] = None,
        title: Optional[str] = None,
    ) -> str:
        """
        Generate a single-dyad analysis report.

        Parameters: results (AnalysisResults), filepath, title.
        Returns: path to written HTML file.
        """
        if filepath is None:
            self.output_dir.mkdir(parents=True, exist_ok=True)
            filepath = str(self.output_dir / f"{results.dyad_id}_report.html")

        title = title or f"Synchrony Analysis Report — {results.dyad_id}"
        body = f"<h1>{title}</h1>\n"
        body += f"<p style='text-align:center;color:#666'>Dyad ID: <code>{results.dyad_id}</code></p>\n"

        # --- Parameters ---
        body += "<h2>Analysis parameters</h2>\n"
        params = results.parameters or {}
        param_rows = [{"Parameter": k, "Value": str(v)} for k, v in params.items()]
        body += _html_table(param_rows)

        # --- Dynamic Features ---
        body += "<h2>Dynamic synchrony features</h2>\n"
        feat = results.dynamic_features
        if feat:
            feat_rows = []
            for pair_key, pair_feats in feat.items():
                for feat_name, val in pair_feats.items():
                    if val is not None and not (isinstance(val, float) and np.isnan(val)):
                        feat_rows.append({
                            "Pair": pair_key,
                            "Feature": feat_name.replace("_", " ").capitalize(),
                            "Value": round(float(val), 4) if isinstance(val, (float, int)) else val,
                        })
            body += _html_table(feat_rows[:40])
        else:
            body += "<p>No dynamic features computed.</p>\n"

        # --- Prediction ---
        body += "<h2>Prediction window analysis</h2>\n"
        pred = results.prediction
        if pred:
            pred_rows = [{
                "Pair": k,
                "Mean Dynamic AUC": round(float(v.get("mean_dynamic_auc", float("nan"))), 4),
                "Mean Baseline AUC": round(float(v.get("mean_baseline_auc", float("nan"))), 4),
                "ΔAUC": round(float(v.get("mean_delta_auc", float("nan"))), 4),
                "Warning": v.get("warning") or "",
            } for k, v in pred.items()]
            body += _html_table(pred_rows)
            body += "<p class='warning'>ΔAUC &gt; 0: dynamic features outperform AR baseline. "
            body += "Bootstrap 95% CI is underestimated when folds &lt; 10.</p>\n"

        # --- Diagnostics ---
        body += "<h2>Pipeline diagnostics</h2>\n"
        diags = results.diagnostics
        if diags:
            diag_rows = [{
                "Stage": d.get("stage", "?"),
                "Pair": d.get("pair", "?"),
                "Reason": d.get("reason", "?"),
            } for d in diags]
            body += f"<p class='warning'>{len(diags)} event(s) flagged by the pipeline.</p>\n"
            body += _html_table(diag_rows)
        else:
            body += "<p>No pipeline warnings.</p>\n"

        # Write file
        html = _html_wrap(title, body, __version__)
        path = Path(filepath)
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(html, encoding="utf-8")
        return str(path)

    def group_report(
        self,
        comparison: Any,  # GroupComparisonReport
        filepath: Optional[str] = None,
        title: Optional[str] = None,
    ) -> str:
        """
        Generate a group-level comparison report.

        Parameters: comparison (GroupComparisonReport), filepath, title.
        Returns: path to written HTML file.
        """
        if filepath is None:
            self.output_dir.mkdir(parents=True, exist_ok=True)
            filepath = str(self.output_dir / "group_comparison_report.html")

        la, lb = comparison.label_a, comparison.label_b
        title = title or f"Group Comparison: {la} vs {lb}"
        body = f"<h1>{title}</h1>\n"
        body += (f"<p style='text-align:center;color:#666'>"
                 f"{la} (n={comparison.n_a}) vs {lb} (n={comparison.n_b}) | "
                 f"α={comparison.alpha} | FDR: Benjamini-Hochberg</p>\n")

        # --- Overview figure ---
        body += "<h2>Group comparison overview</h2>\n"
        body += "<div class='figure'>"
        body += _plot_group_comparison(comparison.test_results, la, lb)
        body += "</div>\n"
        body += "<p class='warning'>* = significant after FDR correction.</p>\n"

        # --- Full statistics table ---
        body += "<h2>Full statistical results</h2>\n"
        stat_rows = [{
            "Metric": r.metric.replace("_", " ").capitalize(),
            f"Mean {la}": round(r.mean_a, 4),
            f"Mean {lb}": round(r.mean_b, 4),
            "Effect size": round(r.effect_size, 4) if not np.isnan(r.effect_size) else "—",
            "Test": r.test_name,
            "p (raw)": round(r.p_raw, 4) if not np.isnan(r.p_raw) else "—",
            "p (FDR)": round(r.p_fdr, 4) if not np.isnan(r.p_fdr) else "—",
            "Sig *": "✓" if r.significant_fdr else "",
        } for r in sorted(comparison.test_results, key=lambda x: x.p_raw if not np.isnan(x.p_raw) else 1.0)]
        body += _html_table(stat_rows)

        # --- Per-dyad table (Group A) ---
        body += f"<h2>Individual dyad results — {la}</h2>\n"
        dyad_rows_a = [dr.to_dict() for dr in comparison.dyad_results_a]
        body += _html_table(dyad_rows_a)

        # --- Per-dyad table (Group B) ---
        body += f"<h2>Individual dyad results — {lb}</h2>\n"
        dyad_rows_b = [dr.to_dict() for dr in comparison.dyad_results_b]
        body += _html_table(dyad_rows_b)

        # Write
        html = _html_wrap(title, body, __version__)
        path = Path(filepath)
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(html, encoding="utf-8")
        return str(path)
